#!/usr/bin/env node
/**
 * Extracts CodexBar's monochrome provider icons into a TS module so the plugin
 * can render each provider's logo (tinted with its brand color) on the LCD and
 * key glyphs — matching the CodexBar menubar app.
 *
 *   npm run icons:providers
 *
 * Reads /Applications/CodexBar.app/Contents/Resources/ProviderIcon-<id>.svg,
 * keeps each icon's viewBox + inner markup, and writes src/core/render/
 * providerIcons.ts. Icons are CodexBar's bundled brand glyphs; this vendors a
 * copy so the plugin is self-contained.
 */
import { readFileSync, writeFileSync, existsSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const here = dirname(fileURLToPath(import.meta.url));
const RES = "/Applications/CodexBar.app/Contents/Resources";

// Providers we map to brand colors / agents; extend as needed.
const PROVIDERS = [
  "codex", "openai", "claude", "minimax", "gemini", "kimi", "grok",
  "copilot", "cursor", "deepseek", "mistral", "factory", "opencode",
];

function extract(svg) {
  const viewBox = (svg.match(/viewBox="([^"]+)"/) || [])[1] || "0 0 24 24";
  let inner = svg
    .replace(/<\?xml[\s\S]*?\?>/g, "")
    .replace(/<svg[^>]*>/i, "")
    .replace(/<\/svg>\s*$/i, "")
    .replace(/<title>[\s\S]*?<\/title>/gi, "")
    // Drop a root-level fill:none so our tint color shows through.
    .replace(/fill="none"/gi, "")
    .trim();
  return { viewBox, body: inner };
}

const out = {};
for (const id of PROVIDERS) {
  const f = join(RES, `ProviderIcon-${id}.svg`);
  if (!existsSync(f)) continue;
  out[id] = extract(readFileSync(f, "utf8"));
}

const ts = `// AUTO-GENERATED by scripts/gen-provider-icons.mjs — do not edit by hand.
// Monochrome provider glyphs vendored from CodexBar (tinted at render time).
export interface ProviderIcon {
  viewBox: string;
  body: string;
}

export const PROVIDER_ICONS: Record<string, ProviderIcon> = ${JSON.stringify(out, null, 2)};

/** Inline a provider icon as an SVG group, scaled to box and tinted \`color\`. */
export function providerIconSvg(
  provider: string,
  x: number,
  y: number,
  size: number,
  color: string,
): string {
  const icon = PROVIDER_ICONS[provider.toLowerCase()];
  if (!icon) return "";
  return \`<svg x="\${x}" y="\${y}" width="\${size}" height="\${size}" viewBox="\${icon.viewBox}"><g fill="\${color}">\${icon.body}</g></svg>\`;
}
`;

const dest = join(here, "..", "src", "core", "render", "providerIcons.ts");
writeFileSync(dest, ts);
console.log(`Wrote ${Object.keys(out).length} provider icons → ${dest}`);
console.log("  " + Object.keys(out).join(", "));
